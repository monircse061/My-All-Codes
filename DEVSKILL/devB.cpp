#include<bits/stdc++.h>

using namespace std;
int main()
{
    char s[100000];

    while(scanf("%s",s)!=EOF)
    {
       /// never be so happy when you are happy and never be so sad when you are sad
        if(strcmp(s,"never")==0)
            cout<<"YES"<<endl;
       else if(strcmp(s,"be")==0)
            cout<<"YES"<<endl;
       else if(strcmp(s,"so")==0)
            cout<<"YES"<<endl;
       else if(strcmp(s,"happy")==0)
            cout<<"YES"<<endl;
       else if(strcmp(s,"when")==0)
            cout<<"YES"<<endl;
       else if(strcmp(s,"you")==0)
            cout<<"YES"<<endl;
        else if(strcmp(s,"are")==0)
            cout<<"YES"<<endl;
       else if(strcmp(s,"and")==0)
            cout<<"YES"<<endl;
        else if(strcmp(s,"sad")==0)
            cout<<"YES"<<endl;
            else
                cout<<"NO"<<endl;
    }
    return 0;
}
